#include <iostream>

using namespace std;

int main() {
    // penjumlahan
    cout << "3+3=" << 3 + 3 << endl;

    // pengurangan
    cout << "4-3=" << 4 - 3 << endl;

    // perkalian
    cout << "4x3=" << 4 * 3 << endl;

    // pembagian
    cout << "100/10=" << 100 / 10 << endl;

    // modulus
    cout << "5 mod 2=" << 5 % 2 << endl;
}