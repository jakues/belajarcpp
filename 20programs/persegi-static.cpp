#include <iostream>

using namespace std;

int main() {
    int p=10; // p = panjang dari persegi, persegi panjang, jajargenjang.
    int l=5; // l = lebar persegi atau persegi panjang && tinggi jajargenjang.

    // luas persegi
    cout << "Luas bangun tersebut adalah " << p*l;
    // keliling persegi
    cout << "Keliling bangun tersebut adalah " << 2*(p+l);
}