#include <iostream>
#define phi 22/7

using namespace std;

int main() {
    float r=7;

    // Luas lingkaran = phi*r^2
    cout << "Luas lingkaran tersebut adalah : " << phi*r*r;
    cout << endl;
    // Keliling lingkaran = phi*2*r
    cout << "Keliling lingkaran tersebut adalah : " << phi*2*r; 
}